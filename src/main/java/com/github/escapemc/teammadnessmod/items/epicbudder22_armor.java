package com.github.escapemc.teammadnessmod.items;

import com.github.escapemc.teammadnessmod.Reference;

import net.minecraft.inventory.EntityEquipmentSlot;
import net.minecraft.item.ItemArmor;

public class epicbudder22_armor extends ItemArmor {

	public epicbudder22_armor(ArmorMaterial materialIn, int renderIndexIn, EntityEquipmentSlot equipmentSlotIn) {
		super(materialIn, renderIndexIn, equipmentSlotIn);

		if(equipmentSlotIn == EntityEquipmentSlot.HEAD) {
			setUnlocalizedName(Reference.ModItems.EPICBUDDER22_HELMET.getUnlocalizedName());
			setRegistryName(Reference.ModItems.EPICBUDDER22_HELMET.getRegistryName());

		
		
		}
			
		if(equipmentSlotIn == EntityEquipmentSlot.CHEST) {
			setUnlocalizedName(Reference.ModItems.EPICBUDDER22_CHESTPLATE.getUnlocalizedName());
			setRegistryName(Reference.ModItems.EPICBUDDER22_CHESTPLATE.getRegistryName());

		
		
		}

		if(equipmentSlotIn == EntityEquipmentSlot.LEGS) {
			setUnlocalizedName(Reference.ModItems.EPICBUDDER22_LEGGINGS.getUnlocalizedName());
			setRegistryName(Reference.ModItems.EPICBUDDER22_LEGGINGS.getRegistryName());

		
		
		}

		if(equipmentSlotIn == EntityEquipmentSlot.FEET) {
			setUnlocalizedName(Reference.ModItems.EPICBUDDER22_BOOTS.getUnlocalizedName());
			setRegistryName(Reference.ModItems.EPICBUDDER22_BOOTS.getRegistryName());

		
		
		}
}

}
