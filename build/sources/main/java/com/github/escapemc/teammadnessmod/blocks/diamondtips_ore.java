package com.github.escapemc.teammadnessmod.blocks;

import com.github.escapemc.teammadnessmod.Reference;

import net.minecraft.block.Block;
import net.minecraft.block.material.Material;

public class diamondtips_ore extends Block {

	public diamondtips_ore(Material materialIn) {
		super(materialIn);
		this.setHardness(2F);
		this.setHarvestLevel("pickaxe", 3);
		this.setResistance(15);

		
		setUnlocalizedName(Reference.ModItems.DIAMONDTIPS_ORE.getUnlocalizedName());
		setRegistryName(Reference.ModItems.DIAMONDTIPS_ORE.getRegistryName());
	}

}
